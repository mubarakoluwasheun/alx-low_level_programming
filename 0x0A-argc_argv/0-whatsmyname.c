#include <stdio.h>

/**
 * main - A function that prints it's name
 *        follow by a new line
 *
 * @argc: Argument count of the function
 * @argv: Argument vector of the function
 * Return: Always 0(success)
 */

int main(int argc, char *argv[])
{
	(void)argc;

	int j = 0;

	printf("%s\n", argv[j]);
	return (0);
}
